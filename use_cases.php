			<h2 class="sub">Education</h2>
			<p>From language learning to basic math and science skills, LibraryBox can serve educational materials to learners regardless of local connectivity or regional/national censorship. If you can provide power, LibraryBox can provide digital resources for learners to any wifi enabled device.</p>
		  </div>
		  <div class="span4">
		
			  <img alt="image" src="./img/healthcare.jpeg">
			
			<h2 class="sub">Healthcare</h2>
			<p>LibraryBox can provide a platform for distribution of health and first aid information to areas without a reliable electrical grid or Internet access. With the addition of an alternative power source, such as solar, LibraryBox can act as a medical library for a community, accessible from any mobile phone with wifi. </p>
		  </div>
		  <div class="span4">
			
			  <img alt="image" src="./img/censorship.jpeg">
		
			<h2 class="sub">Freedom</h2>
			<p>LibraryBox acts as a hyperlocal digital access point that isn't capable of being controlled or restricted in the traditional ways. Firewalls and other access controls that work to limit the distribution of information on the Internet are bypassed by LibraryBox, allowing educational and other types of information to penetrate areas that would otherwise be unreachable.</p>
		  </div>		  
		</div>
		<hr class="dashed">
	    <div class="row">
	     <div class="span4">
			
			  <img alt="image" src="./img/graffiti.jpeg">
	
			<h2 class="sub">Fun</h2>
			<p>LibraryBox could be used for gaming, as part of a digital scavenger hunt. It could be used as part of a geocache, providing digital files to the cache's finders. Use a LibraryBox as part of a virtual graffiti installation, a digital dead drop, and much, much more.</p>
		  </div>	
		  <div class="span4">
			
			  <img alt="image" src="./img/market.jpeg">
				
			<h2 class="sub">Community</h2>
			<p>In any situation where Internet access is questionable, LibraryBox can fill in the gaps of digital delivery of information. Put a LibraryBox on your library's Bookmobile, and serve ebooks to your patrons as you drive around. Take a LibraryBox with you to your community's Farmer's Market and provide instant access to books, pictures, music, and more.</p>
		  </div>
		  <div class="span4">
		
			  <img alt="image" src="./img/hurricance.jpeg">
		
			<h2 class="sub">Emergency</h2>
			<p>In an emergency situation, regular communication channels may be interrupted. In the case of a serious natural disaster such as a hurricane, forest fire, or tornado, some areas may be without power and Internet for extended periods. LibraryBox can provide access to emergency response information to individuals in these situations cheaply and quickly.</p>
