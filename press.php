			  <h2>Logo Usage</h2>
			  <p>The below are the formal LibraryBox logos, free to use and change under the same Create Commons license as the rest of the site. You can <a href="./img/LibraryBox_Logos.zip">download the Photoshop files for the logo</a>, complete with separated layers and such. There are two layers for every bit of text, one rendered and one as a text layer. The fonts used are: <a href="http://fontzone.net/font-details/birchstd">Birch Std</a> for the "Box" text, <a href="http://www.dafont.com/days.font">Days</a> for the "Library" text, and <a href="http://www.losttype.com/font/?name=geared">Geared Slab</a> for the standard font. </p>
<p>In regular text usage, the intercap should always be used; LibraryBox, not librarybox or Librarybox. The plural for LibraryBox is LibraryBoxen, as in "There are many LibraryBoxen in use around the world."
