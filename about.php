			<h2>Mission</h2>
			<p>LibraryBox is designed to enable the distribution of digital information to areas with little or no Internet access, even off the grid. It does so using open source software and very inexpensive hardware, allowing the distribution of LibraryBox hardware at a far lower cost than other solutions.</p> <hr class="dashed">
		  </div>
		  <div class="span6">
			<h2>Privacy</h2>
			<p>LibraryBox is designed to be safe and secure. No logins are required and no user data is logged. The system is purposely not connected to the Internet in order to prevent tracking and preserve user privacy.
</p> <hr class="dashed">
		  </div>

		    <div class="span12">
			<h2>History of The LibraryBox Project</h2>
			<p>The LibraryBox Project began life as a fork of <a href="http://daviddarts.com/piratebox/">the PirateBox project</a> by <a href="http://daviddarts.com/">Dr. David Darts</a>. I approached Dr. Darts with the idea to make a PirateBox without the Pirate, so to speak, and let him know that I was interested in pursuing a fork for educational and library use. I began by creating a single LibraryBox for the Computers in Libraries conference in 2012, which I called the 1.0 of the project. </p>

			<p>I then worked over the next several months to make that release a publicly reproducible product, and released LibraryBox 1.5 in late 2012. Through the first part of 2013, I continued to look at ways to expand the Project, and in May of 2013 I began putting together what would become <a href="kickstarter.com/projects/griffey/librarybox-20">the LibraryBox Kickstarter project</a>, which launched in early July 2013. The success of the Kickstarter allowed me to enlist the services of the lead developer for the PirateBox project, <a href="http://piratebox.aod-rpg.de/dokuwiki/doku.php">Matthias Strubel</a>, and we began the development of the v2.0 code, which was done in concert with the development of PirateBox 1.0. Some of the code produced by the v2.0 Kickstarter funds was rolled back into the codebase for PirateBox, something of which I'm very proud.</p>

		<p>	In April of 2014, the LibraryBox Project was <a href="http://jasongriffey.net/wp/2014/04/22/librarybox-receives-a-knight-foundation-prototype-grant/">awarded a Knight Foundation Prototype Grant</a> for continued development of the project. The grant provided 6 months of funding for work on the project, and will run through October of 2014. LibraryBox v2.0 was <a href="http://jasongriffey.net/wp/2014/05/01/librarybox-v2-0-release/">officially released in May 2014</a>, and development of the v2.1 began. The v2.1 was officially released on December 29, 2015, after a long development process. </p>
</p>  <hr class="dashed">
		  </div>

		  <div class="span12">
			<h2>Evenly Distributed LLC</h2>
			<p><a href="http://evenlydistributed.net/">Evenly Distributed LLC</a> is Jason Griffey, librarian, technologist, maker and parent. Evenly Distributed is the "parent company" of LibraryBox, and Jason is the Director of the LibraryBox project. While Evenly Distributed is selling LibraryBox as a product, the project is entirely Open Source, and the code is available for anyone who wants to tinker or hack it.</p>

