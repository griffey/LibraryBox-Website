	    <div class="row">
		  <div class="span4">
			
				
			<h2 class="sub">New Hardware</h2>
			<p>Technically, LibraryBox should work on anything that runs Piratebox, and that list is pretty long. But the immediate goal is to test and ensure that the current release is stable on the WR703N, the cousin to the MR3020 in TP-Link's lineup.</p>
		  </div>
		  <div class="span4">
			
			<h2 class="sub">Anonymous Statistics</h2>
			<p>Probably the most frequently requested change is a simple count of downloads. This shouldn't be difficult to implement, but is beyond my personal code writing skills, and I would love to find someone familiar with OpenWRT and Droopy to help out.</p>
		  </div>
		  <div class="span4">
	
			<h2 class="sub">Improved Interface</h2>
			<p>LibraryBox is very simple to use, from the patron side, but the user interface could definitely use some help. It's not as simple as just writing some CSS, though, because of the non-standard web server. This is also a priority, and I would love to have someone volunteer to help out with it.</p>
		  </div>		  
		</div>
		<hr class="dashed">
	    <div class="row">
	     <div class="span4">
		
			<h2 class="sub">Mesh Networking</h2>
			<p>There has been discussion in the Piratebox project of having them be a mesh network, where each individual box can "trade" content with the other boxes it can see. For LibraryBox, this would be a huge benefit for updating, meaning that you could update a fleet of LibraryBoxen my simply updating one, and then letting them "sync" with each other.</p>
		  </div>	
		<div class="span4">
				
			<h2 class="sub">Easier Installation</h2>
			<p>While v1.5 of LibraryBox is much easier to install than v1, it could be easier still. A priority for me is to provide a mechanism for installation that bypasses the need to connection the router to the internet, and instead allows for a totally local installation.</p>
		  </div>
		<div class="span4">
		
			<h2 class="sub"><a href="./hardware.php">Alternative Power</a></h2>
			<p>There has been a lot of interest in using solar power to set up a free-standing LibraryBox installations. I've done some quick-and-dirty calculations on how that might work, as well as some recommendations for hardware. More testing is needed to find a robust solution.</p>
		  </div>
		 	  
